def sumando ( num1, num2, num3):
    resultado = num1+num2+num3
    print(f"El resultado de la operación suma es:{resultado}")

num1=int(input("Ingrese el primer numero a calcular:  "))
num2=int(input("Ingrese el segundo numero a calcular:  "))
num3=int(input("Ingrese el tercer numero a calcular:  "))
sumando (num1,num2,num3)

